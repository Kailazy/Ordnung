/// What the bottom player bar needs to render the current track.
struct NowPlaying {
    id: Id,
    artist: String,
    title: String,
    source_path: String,
}

/// State for the inline text box that names a sidebar playlist.
struct Renaming {
    /// The playlist (or folder) being edited.
    id: Id,
    /// Live edit buffer bound to the `TextEdit`.
    buf: String,
    /// True when the row was just created, so an empty/cancelled edit removes it
    /// instead of leaving a blank placeholder behind.
    is_new: bool,
    /// Set on the frame the editor appears so we grab focus exactly once. If we
    /// re-requested focus every frame the box could never lose focus, so
    /// clicking away to commit/cancel would be impossible.
    needs_focus: bool,
}

/// A catalog-mutating request raised by the left sidebar, applied after the
/// panel closure ends so it doesn't borrow `self` while the tree is rendering.
enum SidebarAction {
    /// Create a playlist under the given parent folder (`None` = top level).
    NewPlaylist(Option<Id>),
    Rename(Id, String),
    Delete(Id),
    /// Tracks dropped onto a playlist (playlist id, dragged track ids).
    AddTracks(Id, Vec<Id>),
}

/// A request raised by a track row's right-click context menu. Collected inside
/// the table closure (which only borrows `self` immutably) and applied after the
/// table, where the `&mut self` operations it needs are available. The `Vec<Id>`
/// targets are the whole selection when the right-clicked row is part of it, else
/// just that row — mirroring how a drag carries its payload.
enum TrackMenuAction {
    /// Start/stop the snippet preview for this track.
    Preview(Id, PathBuf),
    /// Open the convert modal for this track.
    Convert(Id),
    /// Open the batch-convert dialog for these tracks (the multi-selection).
    ConvertSelected(Vec<Id>),
    /// Analyze the target track(s) — BPM, key, beatgrid, transcode check —
    /// skipping any already analyzed at the current analyzer version.
    Analyze(Vec<Id>),
    /// Link the target tracks into a playlist (playlist id, track ids).
    AddToPlaylist(Id, Vec<Id>),
    /// Unlink the target tracks from the viewed playlist; they stay in the catalog.
    RemoveFromPlaylist(Id, Vec<Id>),
    /// Permanently delete the target tracks from the catalog (every playlist + the
    /// analysis cache). Source files are never touched. Gated behind a confirm.
    DeleteFromCatalog(Vec<Id>),
    /// Reorder the target tracks to the front/back of the viewed playlist.
    MoveToTop(Id, Vec<Id>),
    MoveToBottom(Id, Vec<Id>),
    /// Show the source file in Finder.
    RevealInFinder(PathBuf),
    /// Copy the source file's path to the clipboard.
    CopyPath(String),
    /// Copy "Artist – Title" for the target track(s) to the clipboard, one line
    /// per track, formatted as a Soulseek search query.
    CopyForSoulseek(Vec<Id>),
    /// Open the track's release/album page on Discogs in the browser. Carries
    /// the track id (to look up a previously-fetched release id) and a search
    /// query (artist + album/title) used when no exact release is on file.
    OpenDiscogs(Id, String),
    /// Re-open the release picker for a single track to (re)choose its Discogs
    /// release — applying that release's cover and filling empty tag fields.
    /// Works regardless of whether the track was already fetched.
    EditRelease(Id),
    /// Fetch cover art only for the target track(s): search Discogs, pick a
    /// release per track, cache its cover. Never touches tags.
    FetchArtwork(Vec<Id>),
    /// Fetch full song details for the target track(s): search Discogs, pick a
    /// release per track, cache its cover and fill the track's empty tag fields.
    FetchSongDetails(Vec<Id>),
}

fn default_db_path() -> Option<PathBuf> {
    let home = std::env::var_os("HOME")?;
    let dir = PathBuf::from(home).join(".ordnung");
    std::fs::create_dir_all(&dir).ok()?;
    Some(dir.join("catalog.db"))
}
