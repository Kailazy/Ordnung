/// Messages the worker thread can send back to the UI.
enum JobMsg {
    Status(String),
    /// Determinate progress for the running job: `done` of `total` items
    /// finished. Drives the status-bar progress bar.
    Progress {
        done: usize,
        total: usize,
    },
    Done(String),
    Failed(String),
    /// Per-item failures from a background job: `(item name, reason)`, with a
    /// `title` naming what was attempted (e.g. "Scan", "Write edits"). Sent just
    /// before `Done` whenever a job skipped or errored on any item, so the UI
    /// can show the user exactly what didn't go through and why.
    Failures {
        title: String,
        items: Vec<(String, String)>,
    },
    /// Candidate releases fetched for one track, for the user to pick from.
    ArtworkChoices(ArtworkChoices),
    /// The Discogs username resolved during a vinyl sync, so the UI can persist
    /// it (config) and link to the user's collection page.
    VinylUsername(String),
}

/// Result of a background full-resolution cover load. The decode (potentially a
/// multi-megapixel JPEG) happens off the UI thread; the main thread turns the
/// pixels into a texture. `image` is `None` when the track has no usable cover.
struct CoverLoaded {
    id: Id,
    image: Option<egui::ColorImage>,
}

/// Result of a background "what would this release fill in?" lookup, computed for
/// the "Fetch song data" picker so the user can see the data before committing.
/// Carries the human-readable (field, value) rows to display and the underlying
/// `ReleaseDetail` to reuse on apply (so we don't fetch the release twice).
struct PreviewMsg {
    track_id: Id,
    release_id: String,
    /// Whether this preview was computed in overwrite mode — part of the cache
    /// key, so it must round-trip back from the worker.
    overwrite: bool,
    /// `(field label, value)` for each field this release would write under the
    /// current mode. Empty when the release adds nothing new or the lookup failed.
    fills: Vec<(String, String)>,
    /// `None` when the release lookup failed (network/decode).
    detail: Option<discogs::ReleaseDetail>,
}

