fn main() -> eframe::Result<()> {
    // Pull a repo-root `.env` into the process environment before anything reads
    // it, so a local `DISCOGS_TOKEN=…` line works for `cargo run`/`make run`
    // without exporting it in the shell. Absent file → no-op (Finder launches
    // fall back to the token saved in ~/.ordnung/config.toml); we never override
    // a variable already set in the real environment.
    let _ = dotenvy::dotenv();
    let db_path = default_db_path().unwrap_or_else(|| PathBuf::from("ordnung.db"));
    let mut viewport = egui::ViewportBuilder::default()
        .with_inner_size([1100.0, 680.0])
        .with_min_inner_size([720.0, 420.0])
        .with_title("Ordnung");
    if let Some(icon) = load_app_icon() {
        viewport = viewport.with_icon(icon);
    }
    let opts = eframe::NativeOptions {
        viewport,
        ..Default::default()
    };
    eframe::run_native(
        "Ordnung",
        opts,
        Box::new(move |cc| Ok(Box::new(App::new(db_path, cc.egui_ctx.clone())))),
    )
}

/// Decode the embedded app icon (committed at `assets/icon.png`, generated by
/// `tools/build-app.sh` from `tools/icon.svg`). Returns `None` if the bytes
/// can't be decoded, in which case eframe falls back to its default icon.
fn load_app_icon() -> Option<egui::IconData> {
    let bytes = include_bytes!("../assets/icon.png");
    let img = image::load_from_memory(bytes).ok()?;
    let rgba = img.to_rgba8();
    let (w, h) = rgba.dimensions();
    Some(egui::IconData {
        rgba: rgba.into_raw(),
        width: w,
        height: h,
    })
}

/// One catalog track flattened for display.
struct TrackRow {
    id: Id,
    artist: String,
    title: String,
    album: String,
    genre: String,
    duration: String,
    bpm: String,
    key: String,
    format: Format,
    format_label: String,
    bitrate: String,
    /// Free-text comment from the file's tag (ID3 COMM / Vorbis COMMENT), shown
    /// read-only in the Notes column. Empty string when the track has no comment.
    notes: String,
    /// Pre-formatted relative age for the Added column ("today", "3d ago").
    added: String,
    /// Raw `added_at` unix timestamp (seconds) behind `added`, so the Added
    /// column sorts by true recency rather than the rounded label.
    added_at: i64,
    source_path: PathBuf,
    // Raw values behind the formatted columns, kept so header sorting orders
    // numerically/musically instead of lexically ("100" vs "99", "10A" vs "2A").
    // `None` = unknown ("—" in the cell), and sorts to one end.
    dur_ms: Option<u64>,
    bpm_val: Option<f32>,
    bitrate_val: Option<u32>,
    /// Camelot wheel order: number * 2 + side (B after A within a number).
    key_sort: Option<u16>,
    /// Camelot wheel position behind the `key` label, used to colour the key
    /// chip (harmonic neighbours share a hue). `None` = unanalyzed ("—").
    camelot: Option<Camelot>,
    /// Transcode-quality verdict from spectral analysis. `None` = not analyzed at
    /// v6+ (shows "—"); the Quality column and its sort read from here.
    quality: Option<TranscodeVerdict>,
    /// Detected low-pass cutoff and estimated lossy source, for the cell tooltip.
    quality_cut_hz: Option<f32>,
    quality_src: Option<&'static str>,
    /// Severity rank for sorting (0 clean … 3 likely-lossy); `None` sorts to the end.
    quality_sort: Option<u8>,
    /// Whether this row had embedded cover art at scan time (drives whether we
    /// even try to load a thumbnail from the catalog).
    has_cover: bool,
    /// Whether the catalog has a successfully-fetched external (Discogs)
    /// artwork row for this track. Combined with `has_cover` to decide
    /// whether the cover cell should attempt a load at all.
    has_external_cover: bool,
}

impl TrackRow {
    /// The text the per-column filter matches against for `col` — the same value
    /// the cell shows. Returns `""` for columns without filterable text (Cover),
    /// and the chip label for the Quality column (so e.g. "lossy" matches).
    fn filter_text(&self, col: TableColumn) -> &str {
        match col {
            TableColumn::Cover => "",
            TableColumn::Artist => &self.artist,
            TableColumn::Title => &self.title,
            TableColumn::Album => &self.album,
            TableColumn::Genre => &self.genre,
            TableColumn::Duration => &self.duration,
            TableColumn::Bpm => &self.bpm,
            TableColumn::Key => &self.key,
            TableColumn::Format => &self.format_label,
            TableColumn::Bitrate => &self.bitrate,
            TableColumn::Quality => self.quality.map_or("—", |v| quality_chip(v).0),
            TableColumn::Notes => &self.notes,
            TableColumn::Added => &self.added,
        }
    }
}

/// Cached thumbnail status — `Some(texture)` = ready, `None` = no cover or
/// decode failed (cached so we don't retry every frame).
type CoverEntry = Option<egui::TextureHandle>;

/// Load state of a table-row thumbnail. A *missing* `cover_cache` entry means
/// "not requested yet"; `Loading` means a background decode is in flight (so we
/// don't request it again every frame); `Ready(Some)` is the decoded texture and
/// `Ready(None)` means the track has no usable cover (cached so we never retry).
/// Decoding the PNG and reading the catalog both happen off the UI thread, so a
/// fast scroll never blocks on disk or image decode — the row shows a placeholder
/// until the worker hands back a texture.
enum ThumbState {
    Loading,
    Ready(CoverEntry),
}

/// Which set of tracks the main table shows: the whole catalog ("Library"), the
/// members of one playlist, or the duplicate-finder report. Selected from the
/// left sidebar.
#[derive(Clone, PartialEq, Eq)]
enum LibraryView {
    Library,
    /// The "recently added" inbox — fresh imports that still need analysis and/or
    /// a Discogs song-data fetch. A track expires out of this view automatically
    /// once it's both analyzed (at the current analyzer version) and fetched, so
    /// it reads as a self-clearing to-do list. Backed by the flat track table.
    RecentlyAdded,
    Playlist(Id),
    /// The duplicate finder — rendered as grouped blocks, not the flat table.
    Duplicates,
    /// Tracks whose source file is gone — rendered as a review list, not the table.
    Missing,
    /// The user's Discogs vinyl collection — rendered as a grid of cover art,
    /// not the flat track table. Backed by the local `vinyl_collection` cache.
    Vinyl,
}

/// A sortable table column. The cover column isn't sortable, so it has no
/// variant. Paired with a direction (`true` = ascending) in `App::sort`.
#[derive(Clone, Copy, PartialEq, Eq)]
enum SortColumn {
    Artist,
    Title,
    Album,
    Genre,
    Duration,
    Bpm,
    Key,
    Format,
    Bitrate,
    Quality,
    Notes,
    Added,
}

/// One column of the track table. The cover thumbnail plus the ten data columns
/// are reorderable and show/hideable from the header's right-click menu; the
/// chosen order and hidden set persist to `~/.ordnung/config.toml`. The trailing
/// `remainder` spacer is structural (it just fills slack so the striped rows span
/// the full width) and is intentionally not represented here.
#[derive(Clone, Copy, PartialEq, Eq, Hash)]
enum TableColumn {
    Cover,
    Artist,
    Title,
    Album,
    Genre,
    Duration,
    // Temporarily not shown (omitted from `DEFAULT_ORDER`) while tempo detection
    // is reworked, so it's never constructed right now — keep the variant and its
    // match arms intact for a one-line restore.
    #[allow(dead_code)]
    Bpm,
    Key,
    Format,
    Bitrate,
    Quality,
    /// Free-text notes — the file's comment tag (ID3 COMM / Vorbis COMMENT),
    /// surfaced read-only here. See `Tags::comment` in ordnung-core.
    Notes,
    /// When the track was first scanned into the catalog (`tracks.added_at`),
    /// shown as a relative age ("today", "3d ago"). Sort it descending to see
    /// the most recent import at the top.
    Added,
}

impl TableColumn {
    /// Every column in the default left-to-right order. Doubles as the source of
    /// truth for "which columns exist": the reorder menu lists these, and loading
    /// a saved layout appends any column missing from it (so a config written by
    /// an older build still shows columns added since).
    // NOTE: `Bpm` is intentionally omitted here — the BPM column is hidden for now
    // while tempo detection is being reworked (stored BPM values were cleared too).
    // The `TableColumn::Bpm` variant and all its match arms are kept so re-adding
    // this one line restores the column. Because `from_key`/`load_column_layout`
    // only resolve columns present in this list, a saved layout containing "bpm"
    // also drops it cleanly.
    const DEFAULT_ORDER: [TableColumn; 12] = [
        TableColumn::Cover,
        TableColumn::Artist,
        TableColumn::Title,
        TableColumn::Album,
        TableColumn::Genre,
        TableColumn::Duration,
        TableColumn::Added,
        TableColumn::Key,
        TableColumn::Format,
        TableColumn::Bitrate,
        TableColumn::Quality,
        TableColumn::Notes,
    ];

    /// Stable key persisted to config — deliberately independent of the enum
    /// order and the display label so reordering the enum or renaming a header
    /// can't corrupt a saved layout.
    fn key(self) -> &'static str {
        match self {
            TableColumn::Cover => "cover",
            TableColumn::Artist => "artist",
            TableColumn::Title => "title",
            TableColumn::Album => "album",
            TableColumn::Genre => "genre",
            TableColumn::Duration => "duration",
            TableColumn::Bpm => "bpm",
            TableColumn::Key => "key",
            TableColumn::Format => "format",
            TableColumn::Bitrate => "bitrate",
            TableColumn::Quality => "quality",
            TableColumn::Notes => "notes",
            TableColumn::Added => "added",
        }
    }

    /// Parse a persisted key back into a column, ignoring anything unknown (a key
    /// from a newer build, or a typo'd hand-edited config).
    fn from_key(s: &str) -> Option<TableColumn> {
        TableColumn::DEFAULT_ORDER
            .into_iter()
            .find(|c| c.key() == s)
    }

    /// Display name: the header text, and the row label in the reorder menu. The
    /// cover header draws no text, but the column still needs a name in the menu.
    fn label(self) -> &'static str {
        match self {
            TableColumn::Cover => "Cover",
            TableColumn::Artist => "Artist",
            TableColumn::Title => "Title",
            TableColumn::Album => "Album",
            TableColumn::Genre => "Genre",
            TableColumn::Duration => "Dur",
            TableColumn::Bpm => "BPM",
            TableColumn::Key => "Key",
            TableColumn::Format => "Format",
            TableColumn::Bitrate => "kbps",
            TableColumn::Quality => "Qual",
            TableColumn::Notes => "Notes",
            TableColumn::Added => "Added",
        }
    }

    /// The sort key cycled by clicking this header, or `None` for the (unsortable)
    /// cover column.
    fn sort_column(self) -> Option<SortColumn> {
        Some(match self {
            TableColumn::Cover => return None,
            TableColumn::Artist => SortColumn::Artist,
            TableColumn::Title => SortColumn::Title,
            TableColumn::Album => SortColumn::Album,
            TableColumn::Genre => SortColumn::Genre,
            TableColumn::Duration => SortColumn::Duration,
            TableColumn::Bpm => SortColumn::Bpm,
            TableColumn::Key => SortColumn::Key,
            TableColumn::Format => SortColumn::Format,
            TableColumn::Bitrate => SortColumn::Bitrate,
            TableColumn::Quality => SortColumn::Quality,
            TableColumn::Notes => SortColumn::Notes,
            TableColumn::Added => SortColumn::Added,
        })
    }

    /// This column's egui_extras width spec (the same sizes the table used when
    /// columns were hard-coded). `cover_px` is the thumbnail edge length.
    /// `width` overrides the initial width with the user's saved value (see
    /// `App::column_widths`); `None` uses the per-column default. The Cover column
    /// is a fixed size and ignores the override.
    fn spec(self, cover_px: f32, width: Option<f32>) -> Column {
        let w = |default: f32| width.unwrap_or(default);
        match self {
            TableColumn::Cover => Column::exact(cover_px + 6.0),
            TableColumn::Artist => Column::initial(w(180.0)).at_least(80.0).clip(true),
            TableColumn::Title => Column::initial(w(260.0)).at_least(80.0).clip(true),
            TableColumn::Album => Column::initial(w(180.0)).at_least(60.0).clip(true),
            TableColumn::Genre => Column::initial(w(110.0)).at_least(60.0).clip(true),
            TableColumn::Duration => Column::initial(w(60.0)).at_least(40.0),
            TableColumn::Bpm => Column::initial(w(55.0)).at_least(40.0),
            TableColumn::Key => Column::initial(w(55.0)).at_least(40.0),
            TableColumn::Format => Column::initial(w(60.0)).at_least(50.0),
            TableColumn::Bitrate => Column::initial(w(70.0)).at_least(50.0),
            TableColumn::Quality => Column::initial(w(66.0)).at_least(48.0),
            TableColumn::Notes => Column::initial(w(200.0)).at_least(80.0).clip(true),
            TableColumn::Added => Column::initial(w(90.0)).at_least(60.0),
        }
    }
}

